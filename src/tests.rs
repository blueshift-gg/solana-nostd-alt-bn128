#[test]
fn test_endianness() {
    // Endianness conversion is pure byte reversal per coordinate, so a simple
    // 0,1,2,... pattern exercises it without needing on-curve points.
    let le1: [u8; 64] = core::array::from_fn(|i| i as u8);
    let p1 = G1Point::from_le_bytes(le1);
    assert_eq!(p1.to_le_bytes(), le1);
    assert_eq!(p1, G1Point(le1));
    // BE reverses each 32-byte coordinate: byte i within a chunk swaps with 31-i.
    let be1 = p1.to_be_bytes();
    assert_eq!(be1[0], le1[31]);
    assert_eq!(be1[31], le1[0]);
    assert_eq!(be1[32], le1[63]);
    assert_eq!(be1[63], le1[32]);
    // Round-trips both ways.
    assert_eq!(G1Point::from_be_bytes(be1), p1);
    assert_eq!(G1Point::from_le_bytes(p1.to_le_bytes()), p1);

    let le2: [u8; 128] = core::array::from_fn(|i| i as u8);
    let p2 = G2Point::from_le_bytes(le2);
    assert_eq!(p2.to_le_bytes(), le2);
    // BE reverses each 64-byte Fq2 coordinate.
    let be2 = p2.to_be_bytes();
    assert_eq!(be2[0], le2[63]);
    assert_eq!(be2[63], le2[0]);
    assert_eq!(be2[64], le2[127]);
    assert_eq!(be2[127], le2[64]);
    assert_eq!(G2Point::from_be_bytes(be2), p2);
    assert_eq!(G2Point::from_le_bytes(p2.to_le_bytes()), p2);
}

#[test]
fn test_rejects_invalid_point() {
    // (x, y) = (1, 1) is off-curve (1 != 1 + 3), so the validated host
    // deserialization must reject it rather than compute on garbage.
    let mut bad = [0u8; 64];
    bad[0] = 1; // x = 1 (LE)
    bad[32] = 1; // y = 1 (LE)
    let bad = G1Point(bad);

    // The group ops validate (compression deliberately does not — it only
    // re-encodes, matching the syscall).
    assert!((bad + bad).is_err());
    assert!((bad - bad).is_err());
    assert!((bad * [1u8; 32]).is_err());
}

#[test]
fn test_rejects_invalid_decompress() {
    // A compressed point whose x-coordinate is non-canonical (>= the field
    // modulus q) cannot be deserialized. q's top byte is 0x30, so an x with
    // top byte 0x3F (after clearing the two flag bits) is > q. Decompression
    // must return `Err`, not panic or decode garbage — on chain a panic would
    // burn the whole CU budget instead of failing cleanly.
    let mut bad_g1 = [0xFFu8; 32];
    bad_g1[31] = 0x3F; // flag bits cleared; x = 0x3FFF..FF > q
    assert!(g1_decompress(&bad_g1).is_err(), "g1: x >= q must reject");

    // G2: x = (c0, c1) in Fq2; c1 occupies bytes [32..64] with flags in byte 63.
    let mut bad_g2 = [0xFFu8; 64];
    bad_g2[63] = 0x3F; // c1 = 0x3FFF..FF > q
    assert!(g2_decompress(&bad_g2).is_err(), "g2: c1 >= q must reject");
}

