fn mul_invalid() -> Result<G1Point, AltBn128Error> {
    black_box(BAD_G1) * black_box([1u8; 32])
}

// check_prime_subgroup (scalar-mul by 1) runs the subgroup check the G2-add
// syscall skips, so it rejects the on-curve non-subgroup point on chain.
#[svm_test(error = ProgramError::InvalidArgument)]
fn g2_subgroup_rejects_nonsub() -> Result<(), AltBn128Error> {
    black_box(NONSUB_G2).check_prime_subgroup()
}

// Proof that the decompress syscall does NOT subgroup-check (`Validate::No`):
// an on-curve, non-subgroup point round-trips through compress/decompress.
#[svm_test]
fn g2_decompress_accepts_nonsub() {
    let compressed = g2_compress(black_box(&NONSUB_G2));
    assert!(g2_decompress(black_box(&compressed)).unwrap() == NONSUB_G2);
}

// Decompressing a non-canonical x-coordinate (>= field modulus q; top byte
// 0x3F > q's 0x30 after clearing flag bits) must fail *cleanly* on chain —
// returning `Err` mapped to `InvalidArgument`, not panicking, which in a
// no_std program would exhaust the entire CU budget instead of failing.
#[svm_test(error = ProgramError::InvalidArgument)]
fn g1_decompress_invalid() -> Result<G1Point, AltBn128Error> {
    let mut bad = [0xFFu8; 32];
    bad[31] = 0x3F;
    g1_decompress(black_box(&bad))
}

#[svm_test(error = ProgramError::InvalidArgument)]
fn g2_decompress_invalid() -> Result<G2Point, AltBn128Error> {
    let mut bad = [0xFFu8; 64];
    bad[63] = 0x3F;
    g2_decompress(black_box(&bad))
}

