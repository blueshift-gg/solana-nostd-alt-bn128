/// On-chain correctness of every op in a single program (not a benchmark).
#[svm_test]
fn correctness() {
    assert!((black_box(A) + black_box(B)).unwrap() == A_PLUS_B);
    assert!((black_box(A_PLUS_B) - black_box(B)).unwrap() == A);
    assert!((black_box(MUL_POINT) * black_box(MUL_SCALAR)).unwrap() == MUL_EXPECT);
    assert!((black_box(A) + black_box(A.negate())).unwrap() == G1Point([0u8; 64]));

    assert!((black_box(PAIR0_G2) + black_box(PAIR1_G2)).unwrap() == G2_ADD_EXPECT);
    assert!((black_box(G2_ADD_EXPECT) - black_box(PAIR1_G2)).unwrap() == PAIR0_G2);
    assert!((black_box(PAIR0_G2) * black_box(SCALAR_TWO)).unwrap() == G2_MUL_EXPECT);
    assert!((black_box(PAIR0_G2) + black_box(PAIR0_G2.negate())).unwrap() == G2Point([0u8; 128]));

    // G2 add does not subgroup-check, so it accepts an on-curve non-subgroup
    // point; check_prime_subgroup (mul-by-1) runs the check on a valid point.
    assert!((black_box(NONSUB_G2) + black_box(NONSUB_G2)).is_ok());
    assert!(black_box(PAIR0_G2).check_prime_subgroup().is_ok());

    assert!(
        pairing(black_box(&[(PAIR0_G1, PAIR0_G2), (PAIR1_G1, PAIR1_G2)])).unwrap()
            == PAIRING_EXPECT
    );

    // Compression (manual): output must match the syscall-produced golden, and
    // round-trip through the decompress syscall.
    let c1 = g1_compress(black_box(&G1_UNCOMPRESSED));
    assert!(c1 == G1_COMPRESSED);
    assert!(g1_decompress(black_box(&c1)).unwrap() == G1_UNCOMPRESSED);
    let c2 = g2_compress(black_box(&G2_UNCOMPRESSED));
    assert!(c2 == G2_COMPRESSED);
    assert!(g2_decompress(black_box(&c2)).unwrap() == G2_UNCOMPRESSED);
}

// The group-op syscall must reject an off-curve point on chain. The body is
// just the fallible op: svm-unit-test maps its `Err` to the program result
// (`AltBn128Error` -> `ProgramError::InvalidArgument`), exactly what `op?` would
// surface in a real entrypoint. NOTE: compression does *not* validate the curve
// equation (it re-encodes coordinates), so this is an arithmetic-op guarantee.

#[svm_test(error = ProgramError::InvalidArgument)]
fn add_invalid() -> Result<G1Point, AltBn128Error> {
    black_box(BAD_G1) + black_box(BAD_G1)
}

#[svm_test(error = ProgramError::InvalidArgument)]
fn sub_invalid() -> Result<G1Point, AltBn128Error> {
    black_box(BAD_G1) - black_box(BAD_G1)
}

